CREATE DATABASE UNIVERSITY_MANAGEMENT;
USE UNIVERSITY_MANAGEMENT;

CREATE TABLE USERS (
	USER_ID INT PRIMARY KEY,
    USER_USERNAME VARCHAR(100) UNIQUE,
    USER_PASSWORD VARCHAR(20),
    USER_TYPE VARCHAR(9)
);

CREATE TABLE FACULTIES (
	FACULTY_ID TINYINT PRIMARY KEY AUTO_INCREMENT,
    FACULTY_NAME VARCHAR(100)
);

CREATE TABLE SPECIALIZATIONS (
	SPECIALIZATION_ID SMALLINT PRIMARY KEY AUTO_INCREMENT,
    SPECIALIZATION_NAME VARCHAR (200),
	STUDY_LEVEL VARCHAR(20),
    FACULTY_ID TINYINT,
    CONSTRAINT FK_SPECIALIZATION_FACULTY_ID FOREIGN KEY (FACULTY_ID) REFERENCES FACULTIES (FACULTY_ID)
);

CREATE TABLE STUDENT_GROUPS (
	GROUP_ID SMALLINT PRIMARY KEY,
    GROUP_NR TINYINT,
    STUDY_YEAR TINYINT,
    SPECIALIZATION_ID SMALLINT,
    CONSTRAINT FK_STUDENT_GROUPS_SPECIALIZATION_ID FOREIGN KEY (SPECIALIZATION_ID) REFERENCES SPECIALIZATIONS (SPECIALIZATION_ID)
);

CREATE TABLE ADMINS ( 
	ADMIN_ID INT PRIMARY KEY,
    USER_ID INT,
    LAST_NAME VARCHAR(50),
    FIRST_NAME VARCHAR(100),
    CONSTRAINT FK_ADMINS_USER_ID FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID)
);

CREATE TABLE STUDENTS (
	STUDENT_ID INT PRIMARY KEY,
    USER_ID INT,
    LAST_NAME VARCHAR(50),
    FIRST_NAME VARCHAR(100),
    BIRTH_DATE DATE,
    EMAIL VARCHAR(200),
    PHONE VARCHAR(10),
    GROUP_ID SMALLINT,
    CONSTRAINT FK_STUDENTS_USER_ID FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
	CONSTRAINT FK_STUDENTS_GROUP_ID FOREIGN KEY (GROUP_ID) REFERENCES STUDENT_GROUPS (GROUP_ID)
);

CREATE TABLE PROFESSORS (
	PROFESSOR_ID INT PRIMARY KEY AUTO_INCREMENT,
	USER_ID INT,
    LAST_NAME VARCHAR(50),
    FIRST_NAME VARCHAR(100),
    BIRTH_DATE DATE,
    FACULTY_ID TINYINT,
	EXPERTISE VARCHAR(100),
    CONSTRAINT FK_PROFESSORS_USER_ID FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
    CONSTRAINT FK_PROFESSORS_FACULTY_ID FOREIGN KEY (FACULTY_ID) REFERENCES FACULTIES (FACULTY_ID)
);

CREATE TABLE COURSES (
	COURSE_ID INT PRIMARY KEY AUTO_INCREMENT,
    COURSE_NAME VARCHAR(100),
    SPECIALIZATION_ID SMALLINT,
    PROFESSOR_ID INT,
    CONSTRAINT FK_COURSES_SPECIALIZATION_ID FOREIGN KEY (SPECIALIZATION_ID) REFERENCES SPECIALIZATIONS (SPECIALIZATION_ID),
    CONSTRAINT FK_COURSES_PROFESSOR_ID FOREIGN KEY (PROFESSOR_ID) REFERENCES PROFESSORS (PROFESSOR_ID)
);

CREATE TABLE COURSES_TIMETABLE (
	COURSE_ID INT,
    COURSE_DAY VARCHAR(20),
    COURSE_TIME TIME,
    COURSE_LOCATION VARCHAR(100),
    CONSTRAINT FK_COURSES_TIMETABLE_COURSE_ID FOREIGN KEY (COURSE_ID) REFERENCES COURSES (COURSE_ID),
    CONSTRAINT PK_COURSE_TIMETABLE PRIMARY KEY (COURSE_ID, COURSE_TIME)
);

CREATE TABLE ATTENDANCES (
	STUDENT_ID INT,
    COURSE_ID INT,
    COURSE_DATE DATE,
    ATTENDANCE BOOLEAN,
    CONSTRAINT FK_ATTENDANCES_STUDENT_ID FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS (STUDENT_ID),
    CONSTRAINT FK_ATTENDANCES_COURSE_ID FOREIGN KEY (COURSE_ID) REFERENCES COURSES (COURSE_ID),
    CONSTRAINT PK_ATTENDANCES PRIMARY KEY (STUDENT_ID, COURSE_ID, COURSE_DATE)
);

CREATE TABLE TASKS (
	PROFESSOR_ID INT,
    COURSE_ID INT,
    TASK_DESC VARCHAR(2000),
    CONSTRAINT FK_TASKS_PROFESSOR_ID FOREIGN KEY (PROFESSOR_ID) REFERENCES PROFESSORS (PROFESSOR_ID),
	CONSTRAINT FK_TASKS_COURSE_ID FOREIGN KEY (COURSE_ID) REFERENCES COURSES (COURSE_ID)
);

CREATE TABLE EXAMS (
	COURSE_ID INT,
    EXAM_TYPE VARCHAR(20),
    EXAM_DATE DATETIME,
    CONSTRAINT FK_EXAMS_COURSE_ID FOREIGN KEY (COURSE_ID) REFERENCES COURSES (COURSE_ID)
);

CREATE TABLE GRADES (
	STUDENT_ID INT,
    COURSE_ID INT,
    EXAM_DATE DATE,
    GRADE TINYINT,
    CONSTRAINT FK_GRADES_STUDENT_ID FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS (STUDENT_ID),
    CONSTRAINT FK_GRADES_COURSE_ID FOREIGN KEY (COURSE_ID) REFERENCES COURSES (COURSE_ID),
    CONSTRAINT PK_GRADES PRIMARY KEY (STUDENT_ID, COURSE_ID)
);


